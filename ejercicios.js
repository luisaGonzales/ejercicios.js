function contarCaracter(palabra){
  var arr = [];
  var contarLetra='a';
  var contador= 0;
  for(var i = 0; i<palabra.length; i++){
    for(var j=0; j<palabra[i].length; j++){
      if(palabra[i][j]==contarLetra){
      contador ++;
    }
    }
  } 
  console.log(contador);
  console.log(arr);
}
